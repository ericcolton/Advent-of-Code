def test_part_one():
    data = parse_input(TEST_INPUT)
    puzzle_builder = PuzzleBuilder(data)
    puzzle_builder.build()
    product = puzzle_builder.corner_tile_ids_product()
    assert product == 20899048083289

def test_part_two():
    data = parse_input(TEST_INPUT)
    puzzle_builder = PuzzleBuilder(data)
    puzzle_builder.build()
    ordered_tiles = puzzle_builder.ordered_tiles()
    combined_tile = generate_combined_tile(ordered_tiles)
    count = count_non_sea_monster_positives(combined_tile)
    assert count == 273

